print(f"Задание 2:")
# Выводим -1024, добавляя нули после минуса, длина строки 10 символов
print(f"{-1024:0=10}")
# Выводим отрицательное дробное число, добавляя '#' перед минусом и обрезая количество
# символов до 2
a=-123.7
